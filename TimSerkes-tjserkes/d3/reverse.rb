def reverse(array)
	last = array.length - 1
	first = 0
	new = []
	while first != array.length
		new[first] = array[last]
		first += 1
		last -= 1
	end
	new
end

fruits = ["apples", "bananas", "kiwis","pears"]
print reverse(fruits)


